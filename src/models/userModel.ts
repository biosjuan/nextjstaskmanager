import mongoose from 'mongoose';

const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

// Compile the model if it hasn't been compiled yet
const USER = mongoose.models.users || mongoose.model('users', userSchema);

export default USER;
